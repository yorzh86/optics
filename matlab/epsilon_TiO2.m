if (wl>=0.4)
    % From Devore (from 0.425 to 1.5 um)
    ep_TiO = 5.913+2.441e7/(wl_A^2-0.803e7);
    ep_TiE = 7.197+3.322e7/(wl_A^2-0.843e7);
elseif (wl<0.425)
    indO=find(O_lam>=wl);
    wave_RO=O_lam(indO(1));
    if indO(1)==1
        nO=O_ref(1);
        kO=O_kap(1);
    else
        wave_LO=O_lam(indO(1)-1);
        nO=(O_ref(indO(1))-O_ref(indO(1)-1))/(wave_RO-wave_LO)*(wl-wave_LO)+O_ref(indO(1)-1);
        kO=(O_kap(indO(1))-O_kap(indO(1)-1))/(wave_RO-wave_LO)*(wl-wave_LO)+O_kap(indO(1)-1);
    end
    ep_TiO = (nO+1i*kO)^2;
    
    indE=find(E_lam>=wl);
    wave_RE=E_lam(indE(1));
    if indE(1)==1
        nE=E_ref(1);
        kE=E_kap(1);
    else
        wave_LE=E_lam(indE(1)-1);
        nE=(E_ref(indE(1))-E_ref(indE(1)-1))/(wave_RE-wave_LE)*(wl-wave_LE)+E_ref(indE(1)-1);
        kE=(E_kap(indE(1))-E_kap(indE(1)-1))/(wave_RE-wave_LE)*(wl-wave_LE)+E_kap(indE(1)-1);
    end
    ep_TiE = (nE+1i*kE)^2;
end
    
